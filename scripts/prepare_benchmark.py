#!/usr/bin/env python3
"""Prepare semi-automated benchmark run packets for Context Bouncer.

This script generates prompts/templates only. It does not invoke Codex, Claude,
or any network service.
"""
from __future__ import annotations

import argparse
import json
import subprocess
import sys
from datetime import datetime, timezone
from pathlib import Path

try:
    sys.stdout.reconfigure(encoding="utf-8")
    sys.stderr.reconfigure(encoding="utf-8")
except Exception:
    pass

ARMS = [
    "long_session_baseline",
    "fresh_handoff",
    "fresh_handoff_scoped_review",
]
REQUIRED_INPUTS = [
    "article.md",
    "article.html",
    "publish-checklist.md",
    "cover-generation-log.md",
    "claude-review-old.md",
    "figure-index.md",
]
REQUIRED_EXPECTED = [
    "expected-review-judgment.md",
    "required-fixes.md",
]
FIXED_TASK = "Finish the publishing readiness check for daily-paper-v1."


def rel(path: Path, root: Path) -> str:
    try:
        return str(path.resolve().relative_to(root.resolve())).replace("\\", "/")
    except Exception:
        return str(path)


def git_commit(cwd: Path) -> str:
    try:
        return subprocess.check_output(
            ["git", "rev-parse", "HEAD"], cwd=cwd, text=True, stderr=subprocess.DEVNULL
        ).strip()
    except Exception:
        return "unknown"


def validate_fixture(fixture: Path) -> list[str]:
    missing: list[str] = []
    for name in REQUIRED_INPUTS:
        if not (fixture / "input" / name).is_file():
            missing.append(f"input/{name}")
    for name in REQUIRED_EXPECTED:
        if not (fixture / "expected" / name).is_file():
            missing.append(f"expected/{name}")
    return missing


def arm_strategy(arm: str) -> str:
    if arm == "long_session_baseline":
        return """
Context strategy: simulate a long-session baseline. Continue with broad context and allow old review notes/logs to be present. Record what was actually read.

Read first:
- input/article.md
- input/article.html
- input/publish-checklist.md
- input/cover-generation-log.md
- input/claude-review-old.md
- input/figure-index.md

Do not read expected/ during execution; expected/ is for grading only.
""".strip()
    if arm == "fresh_handoff":
        return """
Context strategy: start a clean session from a minimal handoff. Use only the listed facts and targeted files before requesting more context.

Minimal handoff facts:
- The cover-generation log is authoritative for whether the cover is already generated.
- Claude review issues must be judged before fixing.
- The publish checklist is the acceptance gate.

Read first:
- input/publish-checklist.md
- input/cover-generation-log.md
- input/claude-review-old.md
- input/article.html

Do not read expected/ during execution; expected/ is for grading only.
""".strip()
    if arm == "fresh_handoff_scoped_review":
        return """
Context strategy: Codex remains the primary implementer. Claude receives only a scoped review packet and must cite evidence for every issue.

Codex read first:
- input/publish-checklist.md
- input/cover-generation-log.md
- input/article.html

Claude scoped review packet should include only:
- input/article.md
- input/article.html
- input/publish-checklist.md
- input/claude-review-old.md

Do not read expected/ during execution; do not send expected/ to the review agent. expected/ is for grading only.
Record whether the review packet truncates any file.
""".strip()
    raise ValueError(f"unknown arm: {arm}")


def build_prompt(fixture: Path, out_dir: Path, arm: str, commit: str) -> str:
    input_files = "\n".join(f"- `{rel(fixture / 'input' / name, out_dir)}`" for name in REQUIRED_INPUTS)
    rubric = rel(Path("benchmarks/templates/quality-rubric.md"), out_dir)
    return f"""# Benchmark run prompt: {arm}

Fixture: `{fixture}`
Context Bouncer commit: `{commit}`
Task: {FIXED_TASK}

## Fixed requirements

1. Confirm whether the cover has already been generated by API.
2. Review the publish checklist.
3. Judge Claude's review issues before fixing.
4. Fix only valid issues.
5. Do not reload full paper/PDF unless needed.
6. Produce a final publish-ready summary.

## Arm-specific protocol

{arm_strategy(arm)}

## Fixture input files

{input_files}

## Measurement rules

- Record provider-reported usage as primary evidence: fresh input, cached input, output tokens, cache write/read fields when available, and net cost.
- Do not use Context Bouncer rough token estimates as billing evidence.
- Record turns, wall time, quality score, critical failures, and whether any packet/file was truncated.
- Grade after the run using `{rubric}` plus fixture `expected/` files.
- Include failed runs unless the failure is an unrelated environment outage.

## Result record

After the run, copy the matching object from `result-record-template.json` into `benchmarks/results/daily-paper-v1/` and fill actual values.
"""


def result_template(fixture: Path, commit: str) -> dict[str, object]:
    now = datetime.now(timezone.utc).isoformat()
    return {
        "fixture": str(fixture),
        "arm": "long_session_baseline",
        "context_bouncer_commit": commit,
        "started_at": now,
        "ended_at": None,
        "tool_versions": {"codex": None, "claude_code": None, "python": None, "os": None},
        "models": {"codex": None, "claude": None},
        "usage": {
            "source": "provider-reported usage log required",
            "input_tokens": None,
            "cached_input_tokens": None,
            "output_tokens": None,
            "cache_write_tokens": None,
            "cost_usd": None,
            "turns": None,
            "wall_time_sec": None,
        },
        "quality": {
            "score": 0,
            "quality_pass": False,
            "critical_failures": [],
            "grader": None,
            "notes": "fill after grading; pass requires score >= 8 and no critical failures",
        },
        "packet": {
            "truncated_files": [],
            "approx_token_count_used_for_billing": False,
            "notes": "do not use packet approx-token counts as billing evidence",
        },
        "artifacts": {},
        "limitations": ["single fixture", "single workflow", "no universal claim"],
        "notes": "copy this template per arm; update arm before saving the run record",
    }


def prepare(fixture: Path, out_dir: Path, repo: Path) -> None:
    fixture = fixture.resolve()
    out_dir = out_dir.resolve()
    missing = validate_fixture(fixture)
    if missing:
        joined = "\n".join(f"- {item}" for item in missing)
        raise SystemExit(f"fixture is missing required files:\n{joined}")
    out_dir.mkdir(parents=True, exist_ok=True)
    commit = git_commit(repo)
    for arm in ARMS:
        (out_dir / f"{arm}.md").write_text(build_prompt(fixture, out_dir, arm, commit), encoding="utf-8-sig")
    template = result_template(fixture, commit)
    (out_dir / "result-record-template.json").write_text(
        json.dumps(template, indent=2, ensure_ascii=False) + "\n", encoding="utf-8"
    )
    (out_dir / "README.md").write_text(
        "# Prepared benchmark run\n\n"
        "Generated prompts only. No agent execution or result collection has happened yet.\n"
        "Use one prompt per arm, then fill `result-record-template.json` from provider-reported logs.\n",
        encoding="utf-8",
    )
    print(out_dir)


def build_parser() -> argparse.ArgumentParser:
    parser = argparse.ArgumentParser(description="Prepare Context Bouncer benchmark run packets.")
    parser.add_argument("--fixture", type=Path, required=True, help="Fixture directory such as benchmarks/fixtures/daily-paper-v1")
    parser.add_argument("--out", type=Path, required=True, help="Output directory for generated run prompts/templates")
    return parser


def main(argv: list[str] | None = None) -> int:
    args = build_parser().parse_args(argv)
    repo = Path(__file__).resolve().parents[1]
    prepare(args.fixture, args.out, repo)
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
